package kodlamaioDbWorkshop.entities;

public class Union {
	
	private String unionName;

	public Union(String unionName) {
		super();
		this.unionName = unionName;
	}

	public Union() {
		super();
	}

	public String getUnionName() {
		return unionName;
	}

	public void setUnionName(String unionName) {
		this.unionName = unionName;
	}
	
}
